const BADGE_ICONS = {
  bottle: SocietyBottleIcon,
  dine: SocietyDineIcon,
  moon: SocietyMoonIcon,
  paint: SocietyPaintIcon,
  sport: SocietySportIcon,
  sun: SocietySunIcon,
  ticket: SocietyTicketIcon,
};

export default function GalleryDateExperienceBadge({
  label,
  isDesktopView = false,
}) {
  const normalizedLabel = normalizeLabel(label);
  const displayLabel = getDisplayLabel(normalizedLabel);

  if (!normalizedLabel || !displayLabel) {
    return null;
  }

  const IconComponent = BADGE_ICONS[getIconKey(normalizedLabel)] || SocietyTicketIcon;
  const compactClass =
    displayLabel.length >= 15 ? " date-experience-badge--compact" : "";

  return (
    <div
      className={`date-experience-badge${compactClass}${
        isDesktopView ? " date-experience-badge--desktop" : ""
      }`}
    >
      <span className="date-experience-badge__icon" aria-hidden="true">
        <IconComponent />
      </span>
      <span className="date-experience-badge__text">
        <span className="date-experience-badge__label">{displayLabel}</span>
      </span>
    </div>
  );
}
